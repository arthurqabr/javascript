/*
Operadores lógicos

&& -> AND -> E   = todas as expressões precisam ser verdadeiras para retornar TRUE
|| -> OR  -> OU  = se um dos retornos for TRUE, a expressão irá retornar TRUE
!  -> NOT -> NÃO = inverte o true para false e vice-versa
*/

console.log(true && false && true);
console.log(true || false);
console.log(!true);
